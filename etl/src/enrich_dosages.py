DEFAULT_ADULT = "Дозировка по официальной инструкции и назначению врача"
DEFAULT_CHILD = "Дозировка для детей — по инструкции и возрасту"


def enrich_catalog_file(path: Path = CATALOG_PATH) -> int:
    if not path.exists():
        return 0
    items = json.loads(path.read_text(encoding="utf-8"))
    updated = 0
    for item in items:
        name = item.get("trade_name", "")
        adult, child = DOSAGES.get(name, (DEFAULT_ADULT, DEFAULT_CHILD))
        if item.get("dosage_adult", "").strip() in ("", "По инструкции"):
            item["dosage_adult"] = adult
            updated += 1
        if item.get("dosage_children", "").strip() in ("", "По инструкции"):
            item["dosage_children"] = child
    path.write_text(json.dumps(items, ensure_ascii=False, indent=2), encoding="utf-8")
    return updated


if __name__ == "__main__":
    n = enrich_catalog_file()
    print(f"enrich_dosages: обновлено полей дозировки у записей каталога ({n} adult)")
